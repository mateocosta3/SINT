package p3;

import java.util.*;

public class Bean13 {

	private ArrayList<Song> songs;
	private String country;
	private String id;
	
	public Bean13() {
		this.songs = songs;
		this.country = country;
		this.id = id;
	}
	
	public ArrayList<Song> getSongs() {
		return songs;
	}
	
	public void setSongs(ArrayList<Song> canciones) {
		songs = canciones;
	}
	
	public String getCountry() {
		return country;
	}
	
	public void setCountry(String pais) {
		country = pais;
	}
	
	public String getId() {
		return id;
	}
	
	public void setId(String idalb) {
		id = idalb;
	}
}
