package p4;

public class Song {
	
	private String title;
	private String lang;
	private String genre;
	private String composer;
	private String sid;
	private String duration;
	private String aid;
	private String muml;
	
	public String getTitle() {
		return title;
	}
	
	public String getLang() {
		return lang;
	}
	
	public String getGenre() {
		return genre;
	}
	
	public String getComposer() {
		return composer;
	}
	
	public String getSid() {
		return sid;
	}
	
	public String getDuration() {
		return duration;
	}
	
	public String getAid() {
		return aid;
	}
	
	public String getMuml() {
		return muml;
	}
	
	public void setTitle(String titulo) {
		title = titulo;
	}
	
	public void setLang(String idioma) {
		lang = idioma;
	}
	
	public void setGenre(String genero) {
		genre = genero;
	}
	
	public void setComposer(String compositor) {
		composer = compositor;
	}
	
	public void setSid(String id) {
		sid = id;
	}
	
	public void setDuration(String duracion) {
		duration = duracion;
	}
	
	public void setAid(String id) {
		aid = id;
	}
	
	public void setMuml(String xml) {
		muml = xml;
	}
}
	
	
