package p3;

import java.util.*;

public class Bean12 {

	private ArrayList<Album> albums;
	private String country;
	
	public Bean12() {
		this.albums = albums;
		this.country = country;
	}
	
	public ArrayList<Album> getAlbums() {
		return albums;
	}
	
	public void setAlbums(ArrayList<Album> albumes) {
		albums = albumes;
	}
	
	public String getCountry() {
		return country;
	}
	
	public void setCountry(String pais) {
		country = pais;
	}
}
