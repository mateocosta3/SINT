package p3;

import java.util.*;

public class Bean02 {

	private ArrayList<String> fes;
	private ArrayList<String> fef;
	
	public Bean02() {
		this.fes = fes;
		this.fef = fef;
	}
	
	public ArrayList<String> getFes() {
		return fes;
	}
	
	public ArrayList<String> getFef() {
		return fef;
	}
	
	public void setFes(ArrayList<String> FES) {
		fes = FES;
	}
	
	public void setFef(ArrayList<String> FEF) {
		fef = FEF;
	}
}
